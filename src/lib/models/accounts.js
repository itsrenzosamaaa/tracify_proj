import mongoose from "mongoose";

const account = new mongoose.Schema({
    username: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        required: true,
    }
});

export default mongoose.models.Account || mongoose.model('Account', account);